def main():
    print(f"Generating {len(HANDCRAFTED_100)} test cases...")

    test_cases = []
    for query, source, chapter_match in HANDCRAFTED_100:
        # 判断安全关键
        safety_keywords = ['发烧', '退烧药', '脱水', '误食', '急救', '烫伤', '溺水', '高烧', '抽搐', '窒息']
        is_safety = any(kw in query for kw in safety_keywords)

        # 提取关键词（简单策略：名词短语）
        import re
        keywords = re.findall(r'[^，。！？\s]+?(?:吗|呢|啊|怎么|什么|多少|可以|需要|是不是)', query)
        keywords = [k.rstrip('吗呢啊') for k in keywords if len(k) > 1][:3]
        if not keywords:
            keywords = query.replace('？', '').replace('，', ' ').split()[:3]

        test_cases.append({
            "query": query,
            "expected_source": source,
            "expected_chapters": chapter_match.split('|'),
            "expected_keywords": keywords,
            "is_safety_critical": is_safety,
        })

    # 保存
    output_path = Path(__file__).parent.parent / "data" / "retrieval_test_set_v2_100.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(test_cases, f, ensure_ascii=False, indent=2)

    print(f"Saved to: {output_path}")

    # 统计
    safety_count = sum(1 for tc in test_cases if tc["is_safety_critical"])
    print(f"\nTotal: {len(test_cases)}")
    print(f"Safety-critical: {safety_count}")
    print(f"Normal: {len(test_cases) - safety_count}")


if __name__ == "__main__":
    main()
